namespace InSimDotNet.Packets {
    /// <summary>
    /// Represents the message sound.
    /// </summary>
    public enum MessageSound {
        /// <summary>
        /// Silent.
        /// </summary>
        SND_SILENT,

        /// <summary>
        /// Message.
        /// </summary>
        SND_MESSAGE,

        /// <summary>
        /// System-message.
        /// </summary>
        SND_SYSMESSAGE,

        /// <summary>
        /// Invalid key.
        /// </summary>
        SND_INVALIDKEY,

        /// <summary>
        /// Error.
        /// </summary>
        SND_ERROR,
    }
}
